    public void mostrar () {
        scooter.getControlador().asignarEvento(new ScooterEvento (){
            @Override
            public void onEventExecute(ScooterEvento.Evento evento) {
                switch (evento) {
                    case DESBLOQUEAR:
                        textoConectado.setText("DESBLOQUEADO");
                        botonIzquierda.setEnabled(true);
                        botonDerecha.setEnabled(true);
                        botonAbajo.setEnabled(true);
                        botonArriba.setEnabled(true);
                        break;
                    case BLOQUEAR:
                        textoConectado.setText("BLOQUEADO");
                        botonIzquierda.setEnabled(false);
                        botonDerecha.setEnabled(false);
                        botonAbajo.setEnabled(false);
                        botonArriba.setEnabled(false);
                        break;
                }
            }
        });
        
        actualizarPosicion();
        this.setVisible(true);
    }
    
    private void actualizarPosicion () {
        textoLatitud.setText(scooter.getLatitud()+"");
        textoLongitud.setText(scooter.getLongitud()+"");
    }
    
    private void mover (boolean horizontalmente, boolean aumentar) {
        float mult = aumentar ? 1 : -1;
        if (horizontalmente) {
            scooter.setLongitud(scooter.getLongitud()+ getVelocidad() * mult * 0.000001f);
        } else {
            scooter.setLatitud(scooter.getLatitud()+ getVelocidad() * mult * 0.000001f);
        }
        actualizarPosicion();
    }
    
    private float getVelocidad () {
        return sliderVelocidad.getValue();
    }
    
